// ─── HAIDT → XY ヘルパー ─────────────────────────────────────────────────────
//
//  引数:  h1=ケア/危害  h2=公平/欺き  h3=忠誠/裏切り
//         h4=権威/転覆  h5=純粋/堕落  h6=自由/抑圧
//
//  X軸（経済的自由 0=大きな政府 … 1=小さな政府）
//    raw_X =  h6 − h1 − 0.5×h2 − 0.3×h3 − 0.1×h4
//  Y軸（個人的自由 0=権威主義 … 1=自由主義）
//    raw_Y =  h6 − h4 − 0.5×h5 − 0.3×h3
//
//  正規化:  (raw + 100) / 200  → clamp [0, 1]
//
//  戻り値: { x, y, haidt } をスプレッドして使う
//    例: { name:'...', color:'#xxx', desc:'...', ...hxy(55,45,85,80,70,40) }

function hxy(h1, h2, h3, h4, h5, h6) {
  const clamp = (v, lo=0, hi=1) => Math.min(hi, Math.max(lo, v));
  return {
    x:     clamp((h6 - h1 - 0.5*h2 - 0.3*h3 - 0.1*h4 + 120) / 150),
    y:     clamp((h6 - h4 - 0.5*h5 - 0.3*h3           + 180) / 280),
    haidt: {
      'ケア/危害': h1, '公平/欺き':   h2, '忠誠/裏切り': h3,
      '権威/転覆': h4, '純粋/堕落':   h5, '自由/抑圧':   h6,
    },
  };
}

